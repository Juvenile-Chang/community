package com.minbei.community.dto;


/**
 * Java bean
 * 实体类
 */
public class GithubUser {
    /**
     * name：用户名
     * id：用户id（唯一标识）
     * bio：描述
     */
    private String name;
    private Long id;
    private String bio;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }

}
